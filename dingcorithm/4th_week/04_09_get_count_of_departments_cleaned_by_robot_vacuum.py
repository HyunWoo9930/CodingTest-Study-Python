def get_count_of_departments_cleaned_by_robot_vacuum(r, c, d, room_map):
    count = 0
    stack = [[r + dr[d], c + dc[d]]]
    room_map[r][c] = 1
    while len(stack) != 0:
        next_step = stack.pop()
        if room_map[next_step[0]][next_step[1]] == 0:
            room_map[next_step[0]][next_step[1]] = 1
            count += 1
        available_place = get_available_place(room_map, next_step[0], next_step[1])
        for i in available_place:
            stack.append(i)

    for i in room_map:
        print(i)
    return count


def get_available_place(room_map, r, c):
    available_place = []
    for i in range(4):
        temp_r = r
        temp_c = c
        temp_r += dr[i]
        temp_c += dc[i]
        if room_map[temp_r][temp_c] == 0:
            available_place.append([temp_r, temp_c])
    return available_place


